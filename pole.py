pole=[['*', '*', '*', '*', '*', '*', '*', '*', '*', '*'],
      ['*', '*', '*', '*', '*', '*', '*', '*', '*', '*'],
      ['*', '*', '*', '*', '*', '*', '*', '*', '*', '*'],
      ['*', '*', '*', '*', '*', '*', '*', '*', '*', '*'],
      ['*', '*', '*', '*', '*', '*', '*', '*', '*', '*'],
      ['*', '*', 'O', '*', '*', '*', '*', '*', '*', '*'],
      ['*', '*', '*', '*', '*', '*', '*', '*', '*', '*'],
      ['*', '*', '*', '*', '*', '*', '*', '*', '*', '*'],
      ['*', '*', '*', '*', '*', '*', '*', '*', '*', '*'],
      ['*', '*', '*', '*', '*', '*', '*', '*', '*', '*']]
oy=5
ox=2
while True:
    x=int(input('Введите номер строки:'))
    y=int(input('Введите номер столбца:'))
    if 0<x and x<11 and 0<y and y<11:
        pole[oy][ox]='*'
        oy=y-1
        ox=x-1
        pole[ox][oy]='O'
        for stroka in pole:
            for kletka in stroka:
                print(kletka,end='')
        print()
    else:
        print('Нет такой строки или такого столбца.')